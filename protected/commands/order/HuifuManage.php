	public function createAccount(){
		$sql = "select * from lcs_huifu_acct_info where status=0";
		$list = Yii::app()->lcs_r->createCommand($sql)->queryAll();
		foreach ($list as $item){
			$args = array(
				'p_uid'=>$item['p_uid'],
				'order_id'=>date('YmdHis'),
				'order_date'=>date('Ymd'),
				'method'=>'applyAccount'
			);
			$this->requestApi($args);	
		}			
	}
	/**
	 * 'order_id','order_date','p_uid','bank_id','card_no','card_prov','card_area'
	 */
	public function bindCard(){
		$sql = "select p_uid from lcs_planner_huifu where status=0 and cash_bind_card_id='' and type=0";
		$p_uids = Yii::app()->lcs_r->createCommand($sql)->queryColumn();
		if(empty($p_uids)){
			return false;
		}
		$b_sql = "SELECT uid,bank_name,card_number,city,province FROM lcs_bank_card where uid in (".implode(',',$p_uids).")";
		$list = Yii::app()->lcs_r->createCommand($b_sql)->queryAll();
		foreach ($list as $item){
			$prov_area = $this->getAreaCode($item['province'], $item['city']);
			if(empty($prov_area['p_code']) || empty($prov_area['c_code'])){
				continue;
			}
			$banklist = ChinapnrArea::getUserBankList();
			$bank_id = 0;
			foreach ($banklist as $b){
				if($b['name'] == $item['bank_name']){
					$bank_id = $b['code'];
					break;
				}
			}
			if(empty($bank_id)){
				continue;
			}
			$args = array(
				'p_uid'=>$item['uid'],
				'order_id'=>date('YmdHis'),
				'order_date'=>date('Ymd'),
				'bank_id'=>$bank_id,
				'card_no'=>$item['card_number'],
				'card_prov'=>$prov_area['p_code'],
				'card_area'=>$prov_area['c_code'],
				'method'=>'bindCard'
			);
			$this->requestApi($args);				
		}
	}
	
	/**
	 * 根据lcs_regoin的省市信息获取code
	 * @param type $province_id
	 * @param type $city_id
	 */
	public function getAreaCode($province_id,$city_id){		
		$pro = $this->getRegion($province_id);
		$city = $this->getRegion($city_id);
		$p_name = $pro['name'];
		$city_name = $city['name'];
		$huifu_p_arr = ChinapnrArea::getProv();
		$huifu_c_arr = ChinapnrArea::getArea();
		$prov_area = ChinapnrArea::getProvArea();
		$res = ['p_code'=>'','c_code'=>''];
		foreach (array_flip($huifu_p_arr) as $name=>$code){
			if(stripos($p_name, $name) !== FALSE){
				$res['p_code'] = $code;
				break;
			}
		}
		foreach(array_flip($huifu_c_arr) as $name=>$code){
			if(stripos($city_name, $name) !== FALSE){
				$res['c_code'] = $code;
				break;
			}
		}
		if($city_name == '市辖区' && !empty($res['p_code'])){
			$area_codes = array_keys($prov_area[$res['p_code']]);
			$res['c_code'] = $area_codes[0];
		}
		return $res;
	}
	
	public function getRegion($id){
		return Yii::app()->lcs_r->createCommand("select name from ".$this->tableNameRegion()." where id=".intval($id))->queryRow();
	}
	
	/**
	 * 请求接口
	 * @param array $args
	 * @return type
	 * @throws Exception
	 */
	private function requestApi($args){	
		if(defined('ENV_DEV') && ENV_DEV == 1){
			$url = "http://i.licaishi.sina.com.cn/inner/chinapnrUserManage";
			$_curl = Yii::app()->curl->setTimeOut(10);		
		}else{
			//$url = "http://i.licaishi.sina.com.cn/inner/chinapnrUserManage";
	//		$_curl = Yii::app()->curl->setTimeOut(10);		
			$url = "http://10.13.32.235/inner/chinapnrUserManage";
			$_curl = Yii::app()->curl->setHeaders(array("Host:i.licaishi.sina.com.cn"))->setTimeOut(10);
		}
        $args["access_token"] = CommonUtils::buildAccessToken($args);
		
		$result = $_curl->post($url, $args);
		if(empty($result)){
			throw new Exception("请求接口失败", RespCode::REQ_METH_ERR);
		}
		$result = json_decode($result,true);
		if($result && isset($result['code']) && $result['code']==0){
			return $result['data'];
		}else{
			throw new Exception($result['msg'],$result['code']);
		}			
	}	
}


